package com.bextdev.mprocedure;

public class SqrtOperation4 {

    public double addSqrt(int number, int number2) {
        return Math.sqrt(number) + Math.sqrt(number2);
    }

    public double subtractSqrt(int number, int number2) {
        return Math.sqrt(number) - Math.sqrt(number2);
    }

    public double timesSqrt(int number, int number2) {
        return Math.sqrt(number) * Math.sqrt(number2);
    }

    public double divideSqrt(int number, int number2) {
        return Math.sqrt(number) / Math.sqrt(number2);
    }
}